
package session2;

public class shiftop {
	
public static void main(String[] args) {
	//rightshift divide
	System.out.println(10>>5);
	System.out.println(10>>5); 
	System.out.println(20>>4);
	//leftshift multiply
	System.out.println(10<<5);
	System.out.println(10<<2);
	System.out.println(20<<4);
}
}
