package session3;

public class loopingstatements {
	public static void main(String[] args) {
		int i = 10;
		do {
		System.out.println("Value of i:" +i);	
		i--;
		}
		
		while(i>=1); 
		System.out.println("Final value of i:" +i);
		 
		
		
		for(i=0;i<10;i++)
	{
			
			System.out.println("The value of i:" +i);
	}
		 
		
		 
		 
		
			
		 
			
		
		
	}

}
